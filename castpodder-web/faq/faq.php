      <strong><a name="12" id="12"></a>What is a mirror?  I just want to download the software!</strong><br />  
      <br />
      "Mirror" is techie talk for places that hold identical copies of the program.  In theory, you'll get the fastest download if you pick the mirror that is geographically closest to you.  Other than that, you can pick any mirror and will get the same software.<br />
      <br />
      To select one, click on the icon with the 1s and 0s, directly to the left of number of bytes, in the Download column at the right side of the page.
