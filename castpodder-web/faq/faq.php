      <strong><a name="1" id="1"></a>What is CastPodder? What can I use it for? </strong><br />  
      <br />
      CastPodder is technically a &quot;Media Aggregator,&quot; a program that allows you to select and download audio files from anywhere on the Internet to your desktop. <br />  
      <br />
      CastPodder makes the process easy by helping you select audio files from among the thousands of audio sources on the web and downloading those files to your computer. Once you select a feed or location, it will download those files automatically at times you specify and have the files waiting for you on your computer, so you don't have to spend a lot of time manually selecting and waiting for downloads. You can play your selected audio files using iTunes or other &quot;jukebox&quot; software, or load them on to your iPod or other portable digital media player to play anytime you want. <br />  
      <br />
      <strong><a name="2" id="2"></a>How does CastPodder work? </strong><br />  
      <br />
      CastPodder uses RSS (really simple syndication) to &quot;feed&quot; files to <br />
      your computer. RSS usually involves headlines and text, but we've devised a way to have it move audio files. Once you've installed CastPodder, you can select podcasts to subscribe to in two different ways: Either clicking the selection button to see a directory of available podcasts or enter a URL for a podcast you've found on your own. You can set the scheduling options so that CastPodder scans for new downloads as often as you like, or you can control it manually. When it is done downloading new files, it adds them to your library, all ready to be played or synched to your MP3 device. <br />  
      <strong><br />
      <a name="3" id="3"></a>Do I need an iPod to use CastPodder? </strong><br />  
      <br />
      No, you can use your computer or any other MP3 player. CastPodder is <br />
      platform-independent, so you can use it from virtually any computer and play the files on any MP3 device. <br />  
      <br />
      <strong><a name="4" id="4"></a>What systems does CastPodder run on? </strong><br />  
      <br />
      CastPodder runs on Linux only.<br />  
