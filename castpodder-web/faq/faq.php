      <strong><a name="5" id="5"></a>What are podcasts? </strong><br />  
      <br />
      Podcasts are audio broadcasts created and stored digitally on the Internet. Instead of being broadcast over the airwaves once and lost, like with traditional radio, podcasts were created to be stored and played at the user's convenience. Think of them as radio shows waiting to be downloaded. There are currently thousands of podcasts available covering everything from automobiles to rock 'n roll. <br />  
